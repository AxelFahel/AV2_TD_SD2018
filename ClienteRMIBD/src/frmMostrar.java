    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
       String cod,nom;
       cod= jTextField1.getText();
       nom= jTextField2.getText();
        
        try {
            Registry registro = LocateRegistry.getRegistry("127.0.0.1",1099);
            RMIBD interfaz = (RMIBD) registro.lookup("rmi://localhost:1099/RMIBD");
            interfaz.modificar(cod, nom);
            JOptionPane.showMessageDialog(null,"Modificado com Exito");
            while (modelo.getRowCount() > 0) modelo.removeRow(0);
            jTextField1.setText("");
            jTextField2.setText("");  
            Object [] fila = new Object[2];
                for(int i=0; i<interfaz.mostrar().size(); i++){
                    fila[0]=interfaz.mostrar().get(i).getCodigo();
                    fila[1]=interfaz.mostrar().get(i).getNome();
                    modelo.addRow(fila);
                    jTable1.setModel(modelo);
                }
        } catch (RemoteException | NotBoundException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

