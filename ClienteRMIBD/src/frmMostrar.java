    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        String cod = "";
        int a = jTable1.getSelectedRow();
        
        if(a<0){
        JOptionPane.showMessageDialog(null, "Deve selecionar uma Fila da Tabela");
        }else{
        int confirmar = JOptionPane.showConfirmDialog(null,"Eliminar Registro?" );
            if(JOptionPane.OK_OPTION == confirmar ){
                try {
                    Registry registro = LocateRegistry.getRegistry("127.0.0.1",1099);
                    RMIBD interfaz = (RMIBD) registro.lookup("rmi://localhost:1099/RMIBD");
                    cod = (String) modelo.getValueAt(a,0);
                    interfaz.eliminar(cod);
                    modelo.removeRow(a);
                    JOptionPane.showMessageDialog(null,"Registro Deltado");
                    
                }  catch (RemoteException | NotBoundException e) {
                    JOptionPane.showMessageDialog(null, e);
                  }
            }
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

